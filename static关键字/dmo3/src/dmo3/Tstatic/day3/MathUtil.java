package dmo3.Tstatic.day3;

/*
 * 数学工具类
 */
public class MathUtil {
	
	public static int jiaInt(int a , int b) {
		return a + b;
	}
	public static int chuInt(int a , int b) {
		return a / b;
	}
	public static int jianInt(int a , int b) {
		return a - b;
	}
	public static int chenInt(int a , int b) {
		return a * b;
	}
}

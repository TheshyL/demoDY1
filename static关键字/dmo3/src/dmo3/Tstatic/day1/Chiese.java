package dmo3.Tstatic.day1;

public class Chiese {
	String id;
	String name;
	String guoji;
	int age;

	public Chiese() {

	}// 无参构造方法

	public Chiese(String id, String name, String guoji, int age) {

		this.id = id;
		this.name = name;
		this.guoji = guoji;
		this.age = age;
	}// 有参构造方法

}

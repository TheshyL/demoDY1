public class DoWhiletext01 {
    
    public static void main(String[] args) {
        /*
         * do..while的 循环语句；
         * do{
         *          循环体；
         *    }while(布尔表达式)；
         * 
         * 
         */

         int i = 10;
         do{
            System.out.println(i);
         }while(i>10);
         System.out.println("------------------------");
         int   ie = 1;
         do{
            System.out.println(ie);
            ie++;

         }while(ie<=10);

    }
}
